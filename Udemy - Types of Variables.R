#Integer
#L is in here because R by default decides what type of variable everything is
#Normal variable storage is as double
x <- 2L
#typeof() gives you the type of variable
typeof(x)

#double
y <- 2.5
typeof(y)

z <- 2
typeof(z)

#complex
r <- 3 + 2i
typeof(r)

#character
a <- "h"
typeof(a)

#logical
q1 <- T
typeof(q1)

q2 <- F
typeof(q2)
